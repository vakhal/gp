package edu.cmu.gizmo.management.beans;

import java.io.Serializable;

/**
 * TaskDef is a JavaBean of a Task object. Orignially designed 
 * to pass the task to Robot, however, it is deprecated due to 
 * a design that assigns the whole brain of task execution to 
 * Task Server
 * 
 * @author sjnicklee
 * @deprecated
 */
public class TaskDef implements Serializable {
	/** Task Id **/
	private int taskId;
	
	/** Task Description **/
	private String taskDesc;
	
	/** Task Name **/
	private String taskName;
	
	/** Task Script Name **/
	private String taskScriptName;

	/** Task Id of a dependent Task **/
	private int taskParentId;

	/**
	 * Returns the id of a task
	 * @return TaskId
	 */
	public int getTaskId() {
		return taskId;
	}
	
	/**
	 * Sets the id of a task
	 * @param taskId id of a Task
	 */
	public void setTaskId(int taskId) {
		this.taskId = taskId;
	}
	
	/**
	 * Returns the description of a task
	 * @return description of a task
	 */
	public String getTaskDesc() {
		return taskDesc;
	}
	
	/**
	 * Sets the description of a task
	 * @param taskDesc description of a task
	 */
	public void setTaskDesc(String taskDesc) {
		this.taskDesc = taskDesc;
	}
	
	/**
	 * Returns the name of a task
	 * @return the name of a task
	 */
	public String getTaskName() {
		return taskName;
	}
	
	/**
	 * Sets the name of a task
	 * @param taskName
	 */
	public void setTaskName(String taskName) {
		this.taskName = taskName;
	}
	
	/**
	 * Returns the script name of a task
	 * @return
	 */
	public String getTaskScriptName() {
		return taskScriptName;
	}
	
	/**
	 * Sets the script name of a task
	 * @param taskScriptName
	 */
	public void setTaskScriptName(String taskScriptName) {
		this.taskScriptName = taskScriptName;
	}
	
	/**
	 * Returns the parent id of a task
	 * @return
	 */
	public int getTaskParentId() {
		return taskParentId;
	}
	
	/**
	 * Sets the parent id of a task
	 * @param taskParentId
	 */
	public void setTaskParentId(int taskParentId) {
		this.taskParentId = taskParentId;
	}
	

}
